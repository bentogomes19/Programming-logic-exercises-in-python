#FAÇA UM PROGRAMA QUE CALCULE E MOSTRE A ÁREA DE UM LOSANGO
#SABE-SE QUE: A = (diagonal maior * diagonal menor)/2

diagonal_maior = int(input("Digite o valor da diagonal maior (m): "))
diagonal_menor = int(input("Digite o valor da diagonal menor(m): "))
area_losango = (diagonal_maior * diagonal_menor)/2
print(f"O valor da área do losango: {area_losango}m2")